const oracledb = require('oracledb');
const path = require('path');
require('dotenv').config();

const walletPath = path.resolve(__dirname, 'wallet');

async function migrate() {
    let connection;
    try {
        await oracledb.createPool({
            user: process.env.DB_USER,
            password: process.env.DB_PASSWORD,
            connectString: 'agrimartdb_high',
            configDir: walletPath,
            walletLocation: walletPath,
            walletPassword: process.env.WALLET_PASSWORD
        });

        connection = await oracledb.getConnection();

        // Drop if exists
        try {
            await connection.execute(`DROP TABLE MONTHLY_INVOICES`);
            console.log('Dropped old MONTHLY_INVOICES');
        } catch (e) {
            // Ignore if doesn't exist
        }

        // Create new schema
        const sql = `
            CREATE TABLE MONTHLY_INVOICES (
                ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                INVOICE_NUMBER VARCHAR2(50) NOT NULL UNIQUE,
                CLIENT_ID NUMBER NOT NULL REFERENCES CLIENTS(ID),
                TOTAL_HT NUMBER(10, 2),
                TOTAL_TVA NUMBER(10, 2),
                TOTAL_TTC NUMBER(10, 2),
                STATUS VARCHAR2(20) DEFAULT 'ISSUED',
                CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                DUE_DATE DATE,
                ORDER_IDS VARCHAR2(500) -- List of order IDs or separate mapping table
            )
        `;
        await connection.execute(sql);
        console.log('✅ Created MONTHLY_INVOICES table');

        // Check if ORDER_INVOICES has ORDER_ID or if we should generalize
        // For now, let's keep it as is and use the new table for partials

        await connection.commit();
    } catch (err) {
        console.error('❌ Migration error:', err);
    } finally {
        if (connection) await connection.close();
        try { await oracledb.getPool().close(0); } catch (e) { }
    }
}

migrate();
