const oracledb = require('oracledb');
const path = require('path');
require('dotenv').config();

const walletPath = path.resolve(__dirname, 'wallet');

async function createInvoicesTable() {
    let connection;
    try {
        console.log('Creating ORDER_INVOICES table...');

        await oracledb.createPool({
            user: process.env.DB_USER,
            password: process.env.DB_PASSWORD,
            connectString: 'agrimartdb_high',
            configDir: walletPath,
            walletLocation: walletPath,
            walletPassword: process.env.WALLET_PASSWORD
        });

        connection = await oracledb.getConnection();

        // Check if exists
        try {
            await connection.execute(`SELECT 1 FROM ORDER_INVOICES FETCH FIRST 1 ROWS ONLY`);
            console.log('Table ORDER_INVOICES already exists.');
        } catch (e) {
            if (e.message.includes('ORA-00942')) {
                // Not found, create it
                const sql = `
                    CREATE TABLE ORDER_INVOICES (
                        ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                        INVOICE_NUMBER VARCHAR2(50) NOT NULL UNIQUE,
                        ORDER_ID NUMBER NOT NULL REFERENCES ORDERS(ID),
                        CLIENT_ID NUMBER NOT NULL REFERENCES CLIENTS(ID),
                        TOTAL_HT NUMBER(10, 2),
                        TOTAL_TVA NUMBER(10, 2),
                        TOTAL_TTC NUMBER(10, 2),
                        STATUS VARCHAR2(20) DEFAULT 'ISSUED',
                        CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                        DUE_DATE DATE
                    )
                `;
                await connection.execute(sql);
                console.log('✅ Created ORDER_INVOICES table.');

                await connection.execute(`CREATE INDEX IDX_INV_ORDER ON ORDER_INVOICES(ORDER_ID)`);
            } else {
                console.error(e);
            }
        }

    } catch (err) {
        console.error('❌ Error:', err);
    } finally {
        if (connection) {
            try { await connection.close(); } catch (e) { }
        }
        try { await oracledb.getPool().close(0); } catch (e) { }
    }
}

createInvoicesTable();
