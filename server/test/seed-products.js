const oracledb = require('oracledb');
const path = require('path');
require('dotenv').config();

const walletPath = path.resolve(__dirname, 'wallet');

async function seedProducts() {
    let connection;

    console.log('Seeding products into Oracle Database...');

    try {
        await oracledb.createPool({
            user: process.env.DB_USER,
            password: process.env.DB_PASSWORD,
            connectString: 'agrimartdb_high',
            configDir: walletPath,
            walletLocation: walletPath,
            walletPassword: process.env.WALLET_PASSWORD
        });

        connection = await oracledb.getConnection();
        console.log('✅ Connected to database');

        // Sample products to insert
        const products = [
            { name: 'Pommes Golden', description: 'Pommes Golden fraîches du verger local', category: 'FRUITS', prix: 2.50, unit: 'kg', stock: 100 },
            { name: 'Tomates Cerises', description: 'Tomates cerises biologiques, sucrées et savoureuses', category: 'LEGUMES', prix: 4.99, unit: 'kg', stock: 50 },
            { name: 'Blé Tendre', description: 'Blé tendre de qualité supérieure pour la farine', category: 'CEREALES', prix: 0.80, unit: 'kg', stock: 500 },
            { name: 'Carottes Bio', description: 'Carottes biologiques cultivées sans pesticides', category: 'LEGUMES', prix: 1.99, unit: 'kg', stock: 80 },
            { name: 'Oranges Valencia', description: 'Oranges juteuses importées de Valencia', category: 'FRUITS', prix: 3.50, unit: 'kg', stock: 120 },
            { name: 'Maïs Doux', description: 'Maïs doux frais, idéal pour grillades', category: 'CEREALES', prix: 2.20, unit: 'kg', stock: 200 },
            { name: 'Poires Williams', description: 'Poires Williams mûres à point', category: 'FRUITS', prix: 3.80, unit: 'kg', stock: 60 },
            { name: 'Courgettes', description: 'Courgettes vertes fraîches du potager', category: 'LEGUMES', prix: 2.30, unit: 'kg', stock: 70 },
            { name: 'Avoine', description: 'Flocons d\'avoine bio pour petit-déjeuner', category: 'CEREALES', prix: 1.50, unit: 'kg', stock: 150 },
            { name: 'Fraises Gariguette', description: 'Fraises Gariguette parfumées et sucrées', category: 'FRUITS', prix: 6.99, unit: 'kg', stock: 40 }
        ];

        // Check if products already exist
        const countResult = await connection.execute('SELECT COUNT(*) as cnt FROM products', [], { outFormat: oracledb.OUT_FORMAT_OBJECT });
        const existingCount = countResult.rows[0].CNT;

        if (existingCount > 0) {
            console.log(`ℹ️  Database already has ${existingCount} products. Skipping seed.`);
            console.log('   To re-seed, first delete existing products.');
        } else {
            console.log('Inserting sample products...');

            const insertSql = `
                INSERT INTO products (product_name, description, category, prix_unitaire, unit, stock_disponible, is_active)
                VALUES (:name, :description, :category, :prix, :unit, :stock, 'Y')
            `;

            for (const product of products) {
                await connection.execute(insertSql, {
                    name: product.name,
                    description: product.description,
                    category: product.category,
                    prix: product.prix,
                    unit: product.unit,
                    stock: product.stock
                }, { autoCommit: false });
                console.log(`  ✅ Inserted: ${product.name}`);
            }

            await connection.commit();
            console.log(`\n🎉 Successfully seeded ${products.length} products!`);
        }

        // Verify products
        const verifyResult = await connection.execute(
            'SELECT product_id, product_name, category, prix_unitaire FROM products WHERE is_active = \'Y\' ORDER BY product_name',
            [],
            { outFormat: oracledb.OUT_FORMAT_OBJECT }
        );
        console.log('\n📦 Products in database:');
        verifyResult.rows.forEach(p => {
            console.log(`   - ${p.PRODUCT_NAME} (${p.CATEGORY}) - ${p.PRIX_UNITAIRE}€`);
        });

    } catch (err) {
        console.error('❌ Error:', err.message);
        if (err.message.includes('ORA-00942')) {
            console.log('\n⚠️  The PRODUCTS table does not exist. Please create it first.');
            console.log('   Run the following SQL in your Oracle database:');
            console.log(`
CREATE TABLE products (
    product_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    product_name VARCHAR2(100) NOT NULL,
    description VARCHAR2(500),
    category VARCHAR2(50),
    prix_unitaire NUMBER(10,2) NOT NULL,
    unit VARCHAR2(20) DEFAULT 'kg',
    stock_disponible NUMBER DEFAULT 0,
    image_url VARCHAR2(500),
    is_active CHAR(1) DEFAULT 'Y',
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);
            `);
        }
    } finally {
        if (connection) {
            try { await connection.close(); } catch (e) { }
        }
        try { await oracledb.getPool().close(0); } catch (e) { }
    }
}

seedProducts();
