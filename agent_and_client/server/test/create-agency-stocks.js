const oracledb = require('oracledb');
const db = require('../config/db');

async function migrate() {
    try {
        await db.initialize();
        const connection = await db.getPool().getConnection();
        console.log('Connected to database');

        // 1. Create AGENCE_STOCKS table
        try {
            await connection.execute(`
                CREATE TABLE AGENCE_STOCKS (
                    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    AGENCE_ID NUMBER REFERENCES AGENCES(ID),
                    PRODUCT_ID NUMBER REFERENCES PRODUCTS(PRODUCT_ID),
                    STOCK NUMBER DEFAULT 0,
                    CONSTRAINT UNIQUE_AG_PROD UNIQUE(AGENCE_ID, PRODUCT_ID)
                )
            `);
            console.log('✅ Table AGENCE_STOCKS created');
        } catch (e) {
            if (e.message.includes('ORA-00955')) console.log('ℹ️ Table AGENCE_STOCKS already exists');
            else throw e;
        }

        // 2. Clear existing
        await connection.execute('DELETE FROM AGENCE_STOCKS');

        // 3. Seed
        const agRes = await connection.execute('SELECT COUNT(*) as cnt FROM AGENCES');
        const agCount = agRes.rows[0]; // depends on outFormat but default is array
        const count = Array.isArray(agRes.rows[0]) ? agRes.rows[0][0] : agRes.rows[0].CNT;

        console.log(`Distributing stock across ${count} agencies...`);

        const seedSql = `
            INSERT INTO AGENCE_STOCKS (AGENCE_ID, PRODUCT_ID, STOCK)
            SELECT a.ID, p.PRODUCT_ID, FLOOR(p.STOCK_DISPONIBLE / :count)
            FROM AGENCES a, PRODUCTS p
        `;

        await connection.execute(seedSql, [count], { autoCommit: true });
        console.log('✅ Agency stocks initialized successfully!');

        await connection.close();
        await db.close();
    } catch (err) {
        console.error('Migration error:', err);
        process.exit(1);
    }
}

migrate();
