const oracledb = require('oracledb');
const path = require('path');
const fs = require('fs');
require('dotenv').config();

const walletPath = path.resolve(__dirname, 'wallet');

async function initAgents() {
    let connection;
    try {
        console.log('Initializing Agents and Agences...');

        await oracledb.createPool({
            user: process.env.DB_USER,
            password: process.env.DB_PASSWORD,
            connectString: 'agrimartdb_high',
            configDir: walletPath,
            walletLocation: walletPath,
            walletPassword: process.env.WALLET_PASSWORD
        });

        connection = await oracledb.getConnection();

        // 1. Create AGENCES table
        try {
            await connection.execute(`
                CREATE TABLE AGENCES (
                    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    CODE VARCHAR2(50) NOT NULL UNIQUE,
                    NOM VARCHAR2(100) NOT NULL,
                    ADRESSE VARCHAR2(200),
                    VILLE VARCHAR2(100),
                    CODE_POSTAL VARCHAR2(20),
                    TELEPHONE VARCHAR2(20),
                    EMAIL VARCHAR2(100),
                    DIRECTEUR_ID NUMBER,
                    STATUS VARCHAR2(20) DEFAULT 'ACTIVE',
                    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP
                )
            `);
            console.log('✅ Table AGENCES created');
        } catch (e) {
            if (e.message.includes('ORA-00955')) console.log('ℹ️  Table AGENCES already exists');
            else throw e;
        }

        // 2. Create AGENTS table
        try {
            await connection.execute(`
                CREATE TABLE AGENTS (
                    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    EMAIL VARCHAR2(255) NOT NULL UNIQUE,
                    PASSWORD VARCHAR2(255) NOT NULL,
                    NOM VARCHAR2(100) NOT NULL,
                    PRENOM VARCHAR2(100),
                    TELEPHONE VARCHAR2(20),
                    AGENCE_ID NUMBER REFERENCES AGENCES(ID),
                    ROLE VARCHAR2(20) DEFAULT 'AGENT',
                    STATUS VARCHAR2(20) DEFAULT 'ACTIVE',
                    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
                    LAST_LOGIN TIMESTAMP
                )
            `);
            console.log('✅ Table AGENTS created');
        } catch (e) {
            if (e.message.includes('ORA-00955')) console.log('ℹ️  Table AGENTS already exists');
            else throw e;
        }

        // 3. Add APPROVED_BY_AGENT to CLIENTS
        try {
            await connection.execute(`ALTER TABLE CLIENTS ADD APPROVED_BY_AGENT NUMBER REFERENCES AGENTS(ID)`);
            console.log('✅ Column APPROVED_BY_AGENT added to CLIENTS');
        } catch (e) {
            if (e.message.includes('ORA-01430')) console.log('ℹ️  Column APPROVED_BY_AGENT already exists');
            else throw e;
        }

        // 4. Insert AGENCES
        const agences = [
            { code: 'AG-CASA-01', nom: 'AgriMart Casablanca Centre', ville: 'Casablanca' },
            { code: 'AG-CASA-02', nom: 'AgriMart Casablanca Ain Sebaa', ville: 'Casablanca' },
            { code: 'AG-CASA-03', nom: 'AgriMart Casablanca Maarif', ville: 'Casablanca' }
        ];

        for (const ag of agences) {
            const check = await connection.execute('SELECT ID FROM AGENCES WHERE CODE = :code', [ag.code]);
            if (check.rows.length === 0) {
                await connection.execute(
                    `INSERT INTO AGENCES (CODE, NOM, VILLE) VALUES (:code, :nom, :ville)`,
                    ag,
                    { autoCommit: true }
                );
                console.log(`✅ Inserted Agence: ${ag.nom}`);
            }
        }

        // 5. Insert Sample Agents
        // Hash for 'agent123': $2a$10$8K1p/a0dL1LXMIZoUqM0/.Fe1RQYe3X9X6HHt6G.QD2KhK9pOqHDy
        const passwordHash = '$2a$10$8K1p/a0dL1LXMIZoUqM0/.Fe1RQYe3X9X6HHt6G.QD2KhK9pOqHDy';

        const agents = [
            { email: 'agent.centre@agrimart.ma', nom: 'Bennani', agenceCode: 'AG-CASA-01', role: 'AGENT' },
            { email: 'agent.ainsebaa@agrimart.ma', nom: 'El Amrani', agenceCode: 'AG-CASA-02', role: 'AGENT' },
            { email: 'agent.maarif@agrimart.ma', nom: 'Alaoui', agenceCode: 'AG-CASA-03', role: 'AGENT' },
            { email: 'admin@agrimart.ma', nom: 'Admin', agenceCode: null, role: 'ADMIN' }
        ];

        for (const agent of agents) {
            const check = await connection.execute('SELECT ID FROM AGENTS WHERE EMAIL = :email', [agent.email]);
            if (check.rows.length === 0) {
                let agenceId = null;
                if (agent.agenceCode) {
                    const agRes = await connection.execute('SELECT ID FROM AGENCES WHERE CODE = :code', [agent.agenceCode]);
                    if (agRes.rows.length > 0) agenceId = agRes.rows[0][0]; // or .ID depending on array/object output. Default is array.
                }

                await connection.execute(
                    `INSERT INTO AGENTS (EMAIL, PASSWORD, NOM, AGENCE_ID, ROLE) VALUES (:email, :pass, :nom, :aid, :role)`,
                    { email: agent.email, pass: passwordHash, nom: agent.nom, aid: agenceId, role: agent.role },
                    { autoCommit: true }
                );
                console.log(`✅ Inserted Agent: ${agent.email}`);
            }
        }

        console.log('🎉 Database initialization complete!');

    } catch (err) {
        console.error('❌ Error:', err);
    } finally {
        if (connection) {
            try { await connection.close(); } catch (e) { }
        }
        try { await oracledb.getPool().close(0); } catch (e) { }
    }
}

initAgents();
